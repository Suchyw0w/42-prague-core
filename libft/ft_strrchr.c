#include <string.h>

char	*ft_strrchr(const char *s, int c)
{
	int		i;
	char	*last;

	i = 0;
	last = NULL;
	while (s[i] != '\0')
	{
		if (s[i] == c)
			last = (char *)s;
		i++;
	}
	return (last);
}
